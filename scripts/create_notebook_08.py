        "the steady-state approximation used in earlier phases.\n"
        "\n"
        "### Physics Interpretation\n"
        "\n"
        "The short carrier lifetimes in 4H-SiC ensure rapid establishment of "
        "steady-state carrier distributions within the first few microseconds "
        "of the pulse plateau. The remaining milliseconds of pulse duration "
        "produce a quasi-DC current. This explains why:\n"
        "- CCE is insensitive to pulse shape (rise/fall times)\n"
        "- Inter-pulse memory is absent at ms-scale gaps\n"
        "- The DC solver provides an adequate CCE prediction\n"
        "\n"
        "These conclusions would differ for materials with longer carrier "
        "lifetimes (e.g., Si with $\\tau$ ~ 10 $\\mu$s), where transient effects "
        "would be significant.\n"
        "\n"
